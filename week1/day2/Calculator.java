package week1.day2;

public class Calculator {
	// Addition method.
	public int add(int a, int b, int c) {
		return a + b + c;
	}

	// Multiplication method.
	public int multiply(float a, float b) {
		return (int) (a * b);
	}

	// Subtraction Method
	public void sub() {
		System.out.println("Subtraction : 5-3 =" + (5 - 3));
	}
}
